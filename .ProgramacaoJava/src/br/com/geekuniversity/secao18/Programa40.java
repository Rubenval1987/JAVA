package br.com.geekuniversity.secao18;

/*
REALIZANDO CÁLCULOS COM MATH:
- Essa biblioteca possui vários métodos e constantes estáticas
  para que possamos usar em nossos programas.
- 

 */
public class Programa40 {

	public static void main(String[] args) {
		System.out.println(Math.E);
		
		System.out.println(Math.PI);
		
		System.out.println(Math.sin(45));
		
		System.out.println(Math.cos(45));

		System.out.println(Math.tan(45));
		
		System.out.println(Math.round(Math.PI)); //Arredonda para inteiro
		
		System.out.println(Math.floor(Math.PI)); //Arredonda para real abaixo
		
		System.out.println(Math.ceil(Math.PI)); //Arredonda para real acima
		
		System.out.println(Math.pow(3, 4)); //Potência
		
		System.out.println(Math.round(Math.pow(3, 4))); //Potência inteira
		
		System.out.println(Math.floor(Math.pow(3, 4))); //Potência real abaixo
		
		System.out.println(Math.ceil(Math.pow(3, 4))); //Potência real acima
		
		System.out.println(Math.sqrt(100)); //Radiciação
		
		System.out.println(Math.round(Math.sqrt(100))); //Radiciação inteira
		
	}
	

}
